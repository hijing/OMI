package omi.bean;

public class geoInfoVO {
	private int code;
	private String name;
	private int num;
	private int bikeAC;
	private int childAC;
	private int jaywalkingAC;
	private int wifi;
	private int publics;
	private int cctv;
	
	public int getWifi() {
		return wifi;
	}
	public void setWifi(int wifi) {
		this.wifi = wifi;
	}
	public int getPublics() {
		return publics;
	}
	public void setPublics(int publics) {
		this.publics = publics;
	}
	public int getCctv() {
		return cctv;
	}
	public void setCctv(int cctv) {
		this.cctv = cctv;
	}
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getBikeAC() {
		return bikeAC;
	}
	public void setBikeAC(int bikeAC) {
		this.bikeAC = bikeAC;
	}
	public int getChildAC() {
		return childAC;
	}
	public void setChildAC(int childAC) {
		this.childAC = childAC;
	}
	public int getJaywalkingAC() {
		return jaywalkingAC;
	}
	public void setJaywalkingAC(int jaywalkingAC) {
		this.jaywalkingAC = jaywalkingAC;
	}
	
	
}
